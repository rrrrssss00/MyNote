先免费交代一些背景。从前年起，政保政策有一个大的调整，即必须有某个学区的户口且只能在该学区内政保。比如，要在金融街政保，你要有金融街的户口，只能选金融街的学校。去年起，政保从填列单一志愿改为填列多志愿，去哪个不保证，总体一直在弱化。

那么好了，要在金融街政保，必须有金融街的房子，因为集体户已经清理了，不通过在金融街买房子落不了户。那么为什么广外的政保比金融街多呢？因为广外的房子便宜啊，性价比高啊，所以很多金融街上班的都在广外买了房子，具备了在广外政保的资格。可是广外没几所好小学，大部分人都填了北小广分和红山，有些没保上的就还在划片小学上了。可是到了初中就不一样喽，这些人还有政保资格呀，而且人越来越多，级别越来越高了，六年过去，正处提副局，副处提正处，小兵提副处，都有政保资格了，他们会不会要求通过保障上广外排位表中的好初中呢？当然会的，广外还是不是中介嘴里说的中产阶级首选呢？我不知道。

以上是金融系统的情况，实际上，我们单位统计各学区上交的政保表，广外也确实是最多的，而且比其他区多得多。公务员系统的情况只能更严重，广源小区里住的都是可以政保的。
